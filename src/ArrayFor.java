public class ArrayFor {
    public static void main(String[] args) throws Exception {
        String alunos[] = { "João", "Luana", "Julia", "Eduardo" };
    
        for (int x = 0; x < alunos.length; x++) {
            System.out.println("O aluno no indice x = " + x + " é " + alunos[x]);
        }
    
    }
    }